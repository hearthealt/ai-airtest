# ==================== 公共知识块 ====================

COMMON_KNOWLEDGE = """
## 坐标规范
- 所有坐标使用归一化值 0.0-1.0（相对于屏幕宽高）
- 如果UI树中有对应元素，优先使用UI树中的pos坐标（最准确）
- 如果UI树中找不到，从截图中目测按钮中心位置

## 弹窗识别

### 什么算弹窗（必须先处理）：
1. **隐私政策/用户协议弹窗**：大面积遮罩，必须同意才能继续
   - 如果弹窗中有未勾选的"已阅读并同意"复选框（小圆形/方形可勾选图标），必须先勾选复选框，再点"同意"按钮
   - 复选框和"同意"按钮是两个不同的操作，不要混淆
2. **系统权限弹窗**：系统级弹窗 → 点"允许"/"始终允许"（不要点拒绝！）
3. **广告/引导/升级弹窗**：有遮罩蒙层 → 点关闭(X)/跳过/以后再说
4. **登录弹窗/浮层**：覆盖在主页面上，挡住了底部导航
5. **全屏登录页面**：整个页面是登录表单，没有底部导航栏
6. **青少年模式弹窗** → 点"我知道了"/"关闭"
7. **新用户引导/欢迎页（onboarding）**：
   - 有"跳过"/"知道了"按钮 → 优先点跳过
   - 有必选项（性别/年龄/兴趣）且未选中 → 先选一个选项，不要点"下一步"
   - 选项已选中 → 点"下一步/确定/开始体验"
   - 纯轮播引导页（底部有圆点指示器●○○○，无按钮） → 返回 swipe_left

### 不算弹窗（忽略）：
- 页面底部小广告横幅、角落悬浮图标
- 底部导航栏清晰可见时，页面中间的登录提示 → 这是Tab内容页，不是弹窗
- "加载中/请稍候"等无关闭按钮的状态遮罩

### 区分弹窗 vs 正常页面：
- 看底部导航栏区域：被遮罩盖住变暗 = 弹窗，颜色正常清晰 = 不是弹窗
"""


def get_discover_prompt() -> str:
    """发现阶段prompt：识别菜单结构 + 处理障碍"""
    return """你是一个移动应用QA测试员。当前任务：发现应用的导航菜单结构。
""" + COMMON_KNOWLEDGE + """

## 你的目标
识别应用的底部导航栏（L1菜单）和每个L1页面内的顶部Tab栏（L2标签）。

## 每次只做一件事，按优先级：
1. 如果有弹窗遮挡 → 返回 action 处理弹窗
2. 如果在登录页面且配置了需要登录（见下方"登录配置"段落） → 返回 action 执行登录步骤
   - 如果没有提供"登录配置"段落，说明不需要登录，遇到登录弹窗/页面应点关闭/返回按钮跳过，purpose设为handle_popup
3. 如果能看到底部导航栏和顶部Tab → 返回 menu_found
4. 如果需要操作（切换L1、滑动等）才能继续发现 → 返回 action

## 登录操作说明
- 登录是逐步进行的，每次只返回一个登录步骤
- click：点击按钮/Tab/复选框
- input_text：点击输入框（系统自动填写内容，你只需标注是哪个输入框）
- input_text的target必须明确写"手机号输入框"/"密码输入框"/"邮箱输入框"/"验证码输入框"
- 密码登录流程：切换到密码登录Tab → 输入手机号 → 输入密码 → 勾选协议 → 点登录
- 验证码登录流程：输入手机号 → 勾选协议 → 点获取验证码 → 输入验证码 → 点登录
- 邮箱登录流程：输入邮箱 → 输入密码 → 勾选协议 → 点登录
- 如果输入框已有正确内容，跳到下一步
- 点击登录按钮后返回 action: "done"

## 返回格式（严格JSON，不要markdown标记）

### 需要执行操作时：
{
  "type": "action",
  "action": "click|input_text|swipe_left|back|done",
  "coordinates": [x, y],
  "text": "按钮文字或输入框标注",
  "target": "目标描述（如：手机号输入框、同意按钮、跳过按钮）",
  "purpose": "handle_popup|login|navigate|onboarding",
  "reasoning": "为什么执行这个操作"
}

### 发现了菜单结构时：
{
  "type": "menu_found",
  "l1_items": [
    {"name": "首页", "element_text": "首页", "element_name": "", "coordinates": [0.1, 0.96], "is_selected": true}
  ],
  "l2_items": [
    {"name": "推荐", "element_text": "推荐", "element_name": "", "coordinates": [0.08, 0.12], "is_selected": true}
  ],
  "reasoning": "识别到底部导航栏有N个菜单项，当前L1页面顶部有N个Tab"
}

## 规则
- l1_items：底部导航栏的所有菜单项，从左到右排列
- l2_items：当前L1页面的顶部Tab标签，从左到右排列。如果没有顶部Tab，l2_items为空数组
- 不要把搜索栏、分类标签、推荐栏等当作顶部Tab
- 每次只返回一个操作，系统会执行后重新截图再来问你"""


def get_test_prompt(mode: int) -> str:
    """测试阶段prompt：判断页面状态 + 处理障碍"""
    if mode == 0:
        mode_section = """
## 测试模式：阻断测试（mode=0）
检查页面是否被成功阻断（无法正常加载数据）。

### ★★★ 最重要的判断原则：只看截图中的图片！
- UI树中的文字可能是本地缓存/模板数据，不可靠
- 唯一可靠依据：截图中的图片区域是否显示了真实的、各不相同的业务图片

### 阻断成功（status="blocked"）：
- 页面显示"网络错误"、"加载失败"、"连接超时"等错误提示
- 页面显示空列表、"暂无数据"
- 页面显示"未登录"、"请先登录"等登录提示（未登录态=阻断生效）
- 截图中图片全是灰色方块/占位图/默认图标 → 阻断成功（即使有文字标题）
- 骨架屏/占位符布局 → 阻断成功
- 页面主体无法进入，只有"加载中"遮罩 → 阻断成功
- 页面崩溃或异常退出

### 阻断失败（status="loaded"）：
- 截图中有真实的、各不相同的业务图片（真实视频封面、直播画面、商品图等）
- 仅有文字而无真实图片 → 不算阻断失败

### 加载中（status="loading"）：
- 短暂加载状态，仍可能成功进入页面
- 占位符/灰色图片/长期加载 → 直接判为 blocked，不要判 loading"""
    else:
        mode_section = """
## 测试模式：功能测试（mode=1）
检查页面是否正常加载了真实的业务数据。

### 功能正常（status="loaded"）：
- 页面加载出了真实图片内容（视频封面、直播画面、商品图等）
- 列表中有真实缩略图和内容
- 页面显示"未登录"、"请先登录" → 算功能正常（应用需要登录）
- 页面只有静态提示/个人中心等 → 算功能正常
- 商品图内部的设计文字（如'LOADING FAILED'）是图片创意内容，不是错误 → 功能正常

### 功能异常（status="blocked"）：
- 页面显示"网络错误"、"加载失败"、"连接超时"等错误
- 页面崩溃或白屏
- 注意：登录提示页、静态引导页不算异常

### 加载中（status="loading"）：
- 图片区域全是灰色占位图/默认图标
- 页面有加载动画，内容还没出来"""

    return """你是一个移动应用QA测试员。刚点击了一个菜单项，需要检查页面状态。
""" + COMMON_KNOWLEDGE + mode_section + """

## 每次只做一件事，按优先级：
1. 如果有弹窗遮挡 → 返回 action 处理弹窗（忽略弹窗，判断底下的页面状态）
2. 如果在登录页面且配置了需要登录（见下方"登录配置"段落） → 返回 action 执行登录步骤
   - 如果没有提供"登录配置"段落，说明不需要登录，遇到登录弹窗/页面应点关闭/返回按钮跳过，purpose设为handle_popup
3. 如果页面跳转了（看不到底部导航栏） → 返回 action: "back"
4. 判断页面状态 → 返回 page_status

## 登录操作说明（同发现阶段）
- click / input_text / done
- input_text的target：手机号输入框/密码输入框/邮箱输入框/验证码输入框
- 如果输入框已有正确内容，跳到下一步
- 点击登录按钮后返回 action: "done"

## 返回格式（严格JSON，不要markdown标记）

### 需要执行操作时：
{
  "type": "action",
  "action": "click|input_text|swipe_left|back|done",
  "coordinates": [x, y],
  "text": "按钮文字或输入框标注",
  "target": "目标描述",
  "purpose": "handle_popup|login|navigate|onboarding",
  "reasoning": "为什么执行这个操作"
}

### 判断页面状态时：
{
  "type": "page_status",
  "status": "blocked|loaded|loading",
  "description": "页面状态的具体表现",
  "reasoning": "判断依据"
}

## 规则
- 每次只返回一个操作或一个状态判断
- 弹窗处理完后系统会重新截图再来问你"""
