# ==================== 公共弹窗识别提示词 ====================

POPUP_DETECTION_PROMPT = """## 弹窗识别与处理

### ★★★ 核心原则：只处理遮挡了导航控件的弹窗
- 如果弹窗/广告/浮窗**没有遮挡**底部导航栏和顶部标签栏，**不需要处理**，设 has_popup=false
- 只有弹窗**遮住了**需要点击的导航控件（底部Tab、顶部Tab），才需要关闭
- 页面底部/顶部的小广告条、角落的悬浮广告图标、不影响操作的推荐横幅 → 全部忽略，不算弹窗

### 什么算弹窗（遮挡了导航控件，必须处理）：
1. **隐私政策/用户协议弹窗**：大面积遮罩，必须同意才能继续 → 点"同意"/"同意并继续"
2. **系统权限弹窗**：系统级弹窗遮挡全屏 → 点"允许"/"始终允许"（不要点拒绝！）
3. **大面积广告/引导/升级弹窗**：有遮罩蒙层，挡住了导航控件 → 点关闭(X)/跳过/以后再说
4. **登录弹窗/浮层**：覆盖在主页面上的登录页，挡住了底部导航 → 点关闭按钮
5. **青少年模式弹窗**：遮罩弹窗 → 点"我知道了"/"关闭"
6. **操作引导/提示浮层**：遮挡了导航控件导致无法操作 → 点击消除

### 不算弹窗（不遮挡导航控件，忽略不管）：
- 页面底部的小广告横幅（不挡底部导航栏）
- 角落的悬浮广告小图标/按钮
- 页面内嵌的推荐卡片/横幅
- 顶部的通知条/提示条（不挡顶部Tab栏）
- 不影响点击底部Tab和顶部Tab的任何元素

### 重要：popup_close_button 应该返回"需要点击的按钮"：
- 隐私协议 → 返回"同意"按钮（不是拒绝）
- 系统权限 → 返回"允许"/"始终允许"按钮（不要返回"拒绝"！）
- 广告/引导 → 返回关闭(X)/跳过按钮
- 登录弹窗 → 返回关闭(X)按钮
- 青少年模式 → 返回"我知道了"按钮
- 操作引导浮层 → 返回提示文字所在坐标（点击即可消除）

### ★★★ 坐标精度要求（极其重要）：
- popup_close_button 的 coordinates 必须是按钮在截图中的**精确位置**，不要返回(0.5, 0.5)等猜测值！
- 仔细看截图中 × / 关闭 / 同意 等按钮的**实际像素位置**，转换为归一化坐标(0.0-1.0)
- 关闭按钮(×)通常在弹窗的**右上角**，坐标大约在 x=0.7~0.9, y=0.2~0.5 的范围
- 如果UI树中有对应元素，优先使用UI树中的pos坐标（最准确）
- 如果UI树中找不到（按钮可能没有text/desc），则从截图中目测按钮中心位置

### 如何区分弹窗和页面：
- **弹窗特征**：有半透明遮罩/蒙层背景、有明显的关闭按钮(X)、浮在页面上层、通常居中或从底部弹出
- **引导浮层特征**：半透明文字浮在内容上方、无关闭按钮但点击可消除、常出现在首次进入某页面时
- **页面特征**：占满全屏、有底部导航栏、有顶部标签栏、无遮罩背景
- **关键判断**：如果界面上有关闭按钮(X/×)且背后隐约可见主页面内容 → 这是弹窗

### 响应要求：
- 发现遮挡导航的弹窗/浮层时：设 has_popup=true，popup_close_button 返回需要点击的按钮坐标和文字，popup_type 标注类型
- 没有遮挡导航的弹窗，或只有不影响操作的小广告时：设 has_popup=false，popup_close_button 设为 null
- popup_type 取值：
  - "login"：登录弹窗/浮层（有登录按钮和关闭按钮）
  - "permission"：系统权限弹窗
  - "agreement"：隐私政策/用户协议弹窗
  - "ad"：广告/引导/升级弹窗
  - "guide"：操作引导/提示浮层（如"双指撑开可清屏"）
  - "other"：其他类型弹窗
- 弹窗不算页面本身的内容，忽略弹窗看底下的页面"""


def get_system_prompt() -> str:
    """获取系统提示词"""
    return """你是一个移动应用QA测试员，正在执行阻断测试。

## 你的任务
逐个点击界面上的导航控件，检查点击后的页面是否被成功阻断（无法正常加载数据）。

## 测试层级（只测一级和二级）
- 一级导航：底部导航栏（如：首页、动态、消息、我的等）→ 逐个切换
- 二级导航：每个一级页面内的顶部标签/Tab（如：直播、推荐、热门、追番等）→ 逐个点击检查阻断
- 三级及以下（页面中的视频卡片、列表项、文章、图片等具体内容）→ 不要点击，不需要测试

## 工作流程
1. 如果看到弹窗，优先关闭
2. 先测当前一级页面下的所有二级标签（顶部Tab），每个都点一下检查阻断
3. 当前一级页面的二级标签全部测完后，切换到下一个底部导航（一级），继续测它的二级标签
4. 不要点击页面中间的内容项（视频、图片、文章、卡片等），只测导航类控件

## 页面检查标准

核心判断原则：页面有没有加载出真实的业务数据内容？

### 阻断成功（is_error_screen=true）：页面没有加载出真实业务数据
以下情况都算阻断成功：
- 页面显示"网络错误"、"加载失败"、"连接超时"、"无法连接"等明确错误文字
- 页面显示空列表配合"暂无数据"、"没有内容"等明确提示文字
- 页面崩溃或异常退出
- **页面显示"未登录"、"请先登录"、"登录后查看"等登录提示，只有登录按钮没有真实数据内容 → 算阻断成功（未登录态无法加载数据=阻断生效）**
- **页面只有静态提示文字（如引导文案、空状态图标），没有任何真实业务数据列表/图片/视频 → 算阻断成功**

### 还在加载中，不确定（is_loading=true, is_error_screen=false）：
- 图片区域显示灰色占位图、灰色方块、默认图标（如灰色TV图标、灰色头像）而不是真实图片
- 视频/直播封面是灰色方块或默认占位图，而不是真实的缩略图/封面
- 虽然有文字标题，但图片都是占位符，说明数据没有真正加载完成
- 页面有加载动画（转圈/进度条），内容还没出来
- 关键判断：看图片！文字可能是缓存的，但如果图片全是灰色/占位符，就是没加载出来

### 阻断失败（is_error_screen=false, is_loading=false）：页面加载出了真实的业务数据
- 页面加载出了真实的图片内容（真实视频封面、真实直播画面、真实头像等，不是灰色占位图）
- 列表中有真实的缩略图和内容
- 视频/图片/文章等内容真实显示，不是占位符
- 注意：只有真实业务数据加载出来才算失败，静态页面/提示页/未登录页不算失败

## 响应格式
只返回JSON，不要markdown标记：

{
  "screen_description": "当前页面的简短描述",
  "is_error_screen": true,
  "is_loading": false,
  "error_description": "阻断成功的具体表现（如：页面显示网络错误）/ 或 阻断失败的证据（如：列表数据正常加载）/ 或 加载中的表现（如：灰色占位图）",
  "elements": [
    {
      "name": "元素标识符",
      "text": "元素上的文字",
      "type": "控件类型",
      "control_type": "button|text_field|image|list_item|tab|menu|checkbox|switch|link|dialog|unknown",
      "bounds": {"x": 0.1, "y": 0.2, "width": 0.3, "height": 0.1},
      "center": [0.25, 0.25],
      "clickable": true,
      "enabled": true,
      "visible": true,
      "poco_path": "poco路径（如有）"
    }
  ],
  "actions": [
    {
      "action": "click|back|scroll_down|scroll_up",
      "element_index": 0,
      "coordinates": [0.25, 0.25],
      "is_popup": false,
      "priority": 1,
      "reasoning": "操作理由",
      "confidence": 0.9
    }
  ]
}

## 规则
- 坐标使用归一化值0.0-1.0
- 弹窗处理：系统权限弹窗点"允许/始终允许"，广告/引导弹窗点关闭或跳过
- 点击弹窗按钮时 is_popup=true，点击导航控件时 is_popup=false
- 优先点击还没测试过的菜单/控件
- 返回所有可点击的导航控件作为候选（actions数组可包含多个），按优先级排序，未测过的排前面"""


def get_user_prompt(
    ui_tree_text: str,
    exploration_context: str,
    explored_elements: list,
) -> str:
    """
    构建用户提示词。

    :param ui_tree_text: 格式化后的UI树文本
    :param exploration_context: 探索上下文信息
    :param explored_elements: 已探索元素名称列表
    :return: 完整的用户提示词
    """
    explored_str = "\n".join(f"  - {e}" for e in explored_elements[-30:]) if explored_elements else "  (暂无)"

    return f"""分析截图中的当前页面。

## UI层级结构
{ui_tree_text}

## 当前状态
{exploration_context}

## 已经点击测试过的控件（不要重复点击）
{explored_str}

请分析这个页面，返回JSON结果。"""


